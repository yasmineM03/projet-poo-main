package carte;

/**
 * L'énumération NatureTerrain représente les différents types de terrains présents sur la carte.
 * Elle permet de spécifier la nature d'un terrain dans la simulation, comme l'eau, la forêt, la roche, etc.
 */
public enum NatureTerrain {
    EAU, FORET, ROCHE, TERRAIN_LIBRE, HABITAT
}

